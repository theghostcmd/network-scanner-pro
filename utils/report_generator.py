import json
import csv
import os
from datetime import datetime
from reportlab.lib.pagesizes import letter, A4
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, Image
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import inch
from reportlab.lib import colors
from reportlab.graphics.shapes import Drawing
from reportlab.graphics.charts.barcharts import VerticalBarChart
import matplotlib.pyplot as plt
import pandas as pd
from colorama import Fore, Style
import logging

class ReportGenerator:
    def __init__(self, config):
        self.config = config
        self.report_dir = "reports"
        self.create_report_directory()
    
    def create_report_directory(self):
        """Create reports directory if it doesn't exist"""
        if not os.path.exists(self.report_dir):
            os.makedirs(self.report_dir)
    
    def generate_pdf_report(self, results):
        """Generate comprehensive PDF report"""
        try:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            filename = f"{self.report_dir}/network_scan_report_{timestamp}.pdf"
            
            doc = SimpleDocTemplate(filename, pagesize=A4)
            story = []
            styles = self.get_pdf_styles()
            
            # Title
            title_style = styles['Title']
            story.append(Paragraph("Network Security Assessment Report", title_style))
            story.append(Spacer(1, 12))
            
            # Header information
            story.append(Paragraph(f"Generated by: Network Scanner Pro", styles['Heading2']))
            story.append(Paragraph(f"Created by: GhostCmd", styles['Heading2']))
            story.append(Paragraph(f"Date: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", styles['Heading2']))
            story.append(Spacer(1, 20))
            
            # Executive Summary
            if self.config['report_settings']['include_executive_summary']:
                story.append(Paragraph("Executive Summary", styles['Heading1']))
                exec_summary = self.generate_executive_summary(results)
                story.append(Paragraph(exec_summary, styles['Normal']))
                story.append(Spacer(1, 12))
            
            # Network Discovery Results
            if 'network_discovery' in results:
                story.append(Paragraph("Network Discovery", styles['Heading1']))
                network_data = self.format_network_data(results['network_discovery'])
                if network_data:
                    network_table = Table(network_data, colWidths=[1.5*inch, 2*inch, 2*inch, 1*inch])
                    network_table.setStyle(TableStyle([
                        ('BACKGROUND', (0, 0), (-1, 0), colors.grey),
                        ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
                        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
                        ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
                        ('FONTSIZE', (0, 0), (-1, 0), 12),
                        ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
                        ('BACKGROUND', (0, 1), (-1, -1), colors.beige),
                        ('GRID', (0, 0), (-1, -1), 1, colors.black)
                    ]))
                    story.append(network_table)
                    story.append(Spacer(1, 12))
            
            # Port Scanning Results
            if 'port_scanning' in results:
                story.append(Paragraph("Port Scanning Results", styles['Heading1']))
                port_data = self.format_port_data(results['port_scanning'])
                for target_data in port_data:
                    story.append(Paragraph(f"Target: {target_data['target']}", styles['Heading2']))
                    for protocol, ports in target_data['protocols'].items():
                        story.append(Paragraph(f"Protocol: {protocol.upper()}", styles['Heading3']))
                        if ports:
                            port_table = Table(ports, colWidths=[0.8*inch, 0.8*inch, 1.5*inch, 2*inch])
                            port_table.setStyle(TableStyle([
                                ('BACKGROUND', (0, 0), (-1, 0), colors.lightblue),
                                ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
                                ('GRID', (0, 0), (-1, -1), 1, colors.black)
                            ]))
                            story.append(port_table)
                            story.append(Spacer(1, 6))
            
            # Vulnerability Assessment
            if 'vulnerability_assessment' in results:
                story.append(Paragraph("Vulnerability Assessment", styles['Heading1']))
                vuln_data = self.format_vulnerability_data(results['vulnerability_assessment'])
                if vuln_data:
                    for severity, vulnerabilities in vuln_data.items():
                        story.append(Paragraph(f"{severity.upper()} Severity Vulnerabilities", styles['Heading2']))
                        for vuln in vulnerabilities:
                            story.append(Paragraph(f"• {vuln}", styles['Normal']))
                        story.append(Spacer(1, 6))
            
            # Charts and Statistics
            story.append(Paragraph("Scan Statistics", styles['Heading1']))
            stats = self.generate_statistics(results)
            for stat, value in stats.items():
                story.append(Paragraph(f"{stat}: {value}", styles['Normal']))
            
            # Build PDF
            doc.build(story)
            print(f"{Fore.GREEN}[+] PDF report generated: {filename}{Style.RESET_ALL}")
            return filename
            
        except Exception as e:
            logging.error(f"PDF report generation error: {str(e)}")
            return None
    
