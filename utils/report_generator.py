    def generate_csv_report(self, results):
        """Generate CSV report for data analysis"""
        try:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            filename = f"{self.report_dir}/network_scan_report_{timestamp}.csv"
            
            with open(filename, 'w', newline='') as csvfile:
                writer = csv.writer(csvfile)
                
                # Write header
                writer.writerow(['Section', 'Target', 'Port', 'Protocol', 'Service', 'State', 'Vulnerability', 'Severity', 'Details'])
                
                # Network Discovery
                if 'network_discovery' in results:
                    for host in results['network_discovery']:
                        writer.writerow(['Network Discovery', host['ip'], '', '', '', host['status'], '', '', host['vendor']])
                
                # Port Scanning
                if 'port_scanning' in results:
                    for target, data in results['port_scanning'].items():
                        for protocol, ports in data.get('protocols', {}).items():
                            for port, info in ports.items():
                                writer.writerow(['Port Scan', target, port, protocol, info['service'], info['state'], '', '', info.get('version', '')])
                
                # Vulnerabilities
                if 'vulnerability_assessment' in results:
                    for vuln in results['vulnerability_assessment'].get('vulnerabilities', []):
                        details = vuln['details']
                        writer.writerow([
                            'Vulnerability',
                            vuln.get('service', ''),
                            vuln.get('port', ''),
                            vuln.get('protocol', ''),
                            '',
                            '',
                            details.get('issue', ''),
                            details.get('severity', ''),
                            details.get('details', '')
                        ])
            
            print(f"{Fore.GREEN}[+] CSV report generated: {filename}{Style.RESET_ALL}")
            return filename
            
        except Exception as e:
            logging.error(f"CSV report generation error: {str(e)}")
            return None
    
    def get_pdf_styles(self):
        """Get PDF styles"""
        styles = getSampleStyleSheet()
        
        # Custom styles
        styles.add(ParagraphStyle(
            name='CustomTitle',
            parent=styles['Title'],
            fontSize=24,
            spaceAfter=30,
            textColor=colors.darkblue
        ))
        
        return styles
    
    def format_network_data(self, network_results):
        """Format network data for PDF table"""
        data = [['IP Address', 'MAC Address', 'Vendor', 'Status']]
        for host in network_results:
            data.append([
                host['ip'],
                host['mac'],
                host['vendor'],
                host['status'].upper()
            ])
        return data
    
    def format_port_data(self, port_results):
        """Format port data for PDF"""
        formatted_data = []
        for target, data in port_results.items():
            target_data = {
                'target': target,
                'protocols': {}
            }
            for protocol, ports in data.get('protocols', {}).items():
                protocol_data = [['Port', 'State', 'Service', 'Version']]
                for port, info in ports.items():
                    protocol_data.append([
                        str(port),
                        info['state'].upper(),
                        info['service'],
                        info.get('version', '')
                    ])
                target_data['protocols'][protocol] = protocol_data
            formatted_data.append(target_data)
        return formatted_data
    
    def format_vulnerability_data(self, vuln_results):
        """Format vulnerability data"""
        by_severity = {}
        for vuln in vuln_results.get('vulnerabilities', []):
            severity = vuln['details'].get('severity', 'unknown')
            if severity not in by_severity:
                by_severity[severity] = []
            
            vuln_text = f"{vuln['type']} - {vuln['service']} on port {vuln['port']}: {vuln['details'].get('issue', 'Unknown issue')}"
            if 'cve_id' in vuln['details']:
                vuln_text += f" ({vuln['details']['cve_id']})"
            
            by_severity[severity].append(vuln_text)
        
        return by_severity
    
    def generate_executive_summary(self, results):
        """Generate executive summary"""
        total_hosts = len(results.get('network_discovery', []))
        open_ports = 0
        vulnerabilities = len(results.get('vulnerability_assessment', {}).get('vulnerabilities', []))
        
        if 'port_scanning' in results:
            for target, data in results['port_scanning'].items():
                for protocol, ports in data.get('protocols', {}).items():
                    open_ports += len([p for p in ports.values() if p['state'] == 'open'])
        
        summary = f"""
        This network security assessment discovered {total_hosts} active hosts with {open_ports} open ports. 
        The vulnerability assessment identified {vulnerabilities} potential security issues requiring attention.
        """
        
        if vulnerabilities > 0:
            summary += " Immediate remediation is recommended for high and critical severity vulnerabilities."
        else:
            summary += " No critical vulnerabilities were identified during this assessment."
        
        return summary
    
    def generate_statistics(self, results):
        """Generate scan statistics"""
        stats = {}
        
        # Network stats
        stats['Hosts Discovered'] = len(results.get('network_discovery', []))
        
        # Port stats
        open_ports = 0
        if 'port_scanning' in results:
            for target, data in results['port_scanning'].items():
                for protocol, ports in data.get('protocols', {}).items():
                    open_ports += len([p for p in ports.values() if p['state'] == 'open'])
        stats['Open Ports'] = open_ports
        
        # Vulnerability stats
        vuln_count = len(results.get('vulnerability_assessment', {}).get('vulnerabilities', []))
        stats['Vulnerabilities Found'] = vuln_count
        
        # Services scanned
        services_scanned = results.get('vulnerability_assessment', {}).get('services_scanned', 0)
        stats['Services Scanned'] = services_scanned
        
        return stats
    
    def generate_reports(self, results, output_format="all"):
        """Generate all requested reports"""
        report_paths = {}
        
        if output_format in ["pdf", "all"] and self.config['report_settings']['generate_pdf']:
            report_paths['pdf'] = self.generate_pdf_report(results)
        
        if output_format in ["html", "all"] and self.config['report_settings']['generate_html']:
            report_paths['html'] = self.generate_html_report(results)
        
        if output_format in ["csv", "all"] and self.config['report_settings']['generate_csv']:
            report_paths['csv'] = self.generate_csv_report(results)
        
        return report_paths